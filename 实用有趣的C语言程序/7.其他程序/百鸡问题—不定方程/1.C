#include <stdio.h>
void main()
{
   int x,y,z,n;
   n=0;
   for (x=1;x<=100/5;x++) 
	    for (y=1;y<=100/3;y++) 
			for (z=3;z<=99;z=z+3) 
      	 	if (x+y+z==100&&5*x+3*y+z/3==100)
		{   
			n=n+1;
			printf("  %d: x=%d,y=%d,z=%d \n",n,x,y,z);
	 	    break;
		}
 }
 


