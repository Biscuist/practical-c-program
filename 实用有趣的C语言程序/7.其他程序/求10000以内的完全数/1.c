/*1.c  求10000以内的完全数，4,28,496,8128    */
/**********************************************/
#include "stdio.h"
void main()
{
int j,k,sum = 0;
for(k=2;k<=10000;k++)
{
sum=0;
for(j=1;j<k;j++)
if(k%j==0)
sum=sum+j;
if(sum==k)
printf(" %d ",k);
}
printf("\n ");
}
 